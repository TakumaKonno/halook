package jp.co.acroquest.endosnipe.javelin.converter.hbase.monitor;

import java.util.List;
import java.util.Map;
import java.util.Set;

/**
 * HMasterから情報を取得するために、追加したインタフェース。
 * 
 * @author eriguchi
 */
public interface HMasterAccessor
{
    /**
     * Online状態の全サーバについて、サーバ名をキー、サーバ情報を値とするマップを取得する。
     * 
     * @return サーバ名をキー、サーバ情報を値とするマップ。
     */
    Map<String, HServerInfo> getServerInfo();
    
    /**
     * Offline状態の全サーバについて、サーバ名のセットを取得する。
     * 
     * @return サーバ名のセット。
     */
    Set<String> getDeadServerInfo();
    
    /**
     * 全テーブルのリストを取得する。
     * 
     * @return 全テーブルのリスト。
     */
    List<HTableDescriptor> listTables();

    /**
     * 指定したテーブルのリージョン数を取得する。
     * 
     * @param name テーブル名。
     * @return 指定したテーブルのリージョン数。
     */
    int getRegionsOfTable(byte[] name);
    
    /**
     * サーバ情報をキー、そこに所属するリージョン情報のリストを値とするマップを取得する。
     * 
     * @return サーバ情報をキー、そこに所属するリージョン情報のリストを値とするマップ。
     */
    Map<HServerInfo, List<HRegionInfo>> getAssignments();
}
